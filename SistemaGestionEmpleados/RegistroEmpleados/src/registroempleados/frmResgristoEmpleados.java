    private void btoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoEditarActionPerformed
        // TODO add your handling code here:
      String []datos=new String[5];
      datos[0]=txtCodigo.getText();
      datos[1]=txtEmpleado.getText();
      datos[2]=this.comboCargo.getSelectedItem().toString();
      datos[3]=txtFecha.getText();
      datos[4]=txtSueldo.getText();
      
      for(int i=0; i < TablaEmpleados.getColumnCount(); i++){
          this.model.setValueAt(datos[i], this.filas,i );
       
      }
      
    }//GEN-LAST:event_btoEditarActionPerformed

    private void TablaEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaEmpleadosMouseClicked
        // TODO add your handling code here:
        int fila_seleccionada=TablaEmpleados.getSelectedRow();
        txtCodigo.setText(TablaEmpleados.getValueAt(fila_seleccionada, 0).toString());
        txtEmpleado.setText(TablaEmpleados.getValueAt(fila_seleccionada, 1).toString());
        comboCargo.setSelectedItem(TablaEmpleados.getValueAt(fila_seleccionada, 2).toString());
        txtFecha.setText(TablaEmpleados.getValueAt(fila_seleccionada, 3).toString());
        txtSueldoFinal.setText(TablaEmpleados.getValueAt(fila_seleccionada, 4).toString());
        filas = fila_seleccionada;
    }//GEN-LAST:event_TablaEmpleadosMouseClicked

