    private void comboTipoContratacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboTipoContratacionActionPerformed
        // TODO add your handling code here:
        String tipoContratacion = this.comboTipoContratacion.getSelectedItem().toString();
        Double sueldoFinal = 0D;
        Double sueldoOfrecidoPorHora = 0D; //Double.parseDouble(this.txtSueldo.getText());
        boolean valuar = this.txtSueldo.getText().isEmpty();
        if (valuar) {
            sueldoOfrecidoPorHora = 0D;
        }else{
            sueldoOfrecidoPorHora = Double.parseDouble(this.txtSueldo.getText());
        }
       
        switch (tipoContratacion) {
            case "Parcial":
                sueldoFinal = sueldoOfrecidoPorHora * 4;
                break;
            case "Tiempo completo":
                sueldoFinal = sueldoOfrecidoPorHora * 8;
                break;
            default:
               sueldoFinal = 0D;

                
        
        }
        this.txtSueldoFinal.setText(sueldoFinal.toString());
        
        
        
    }//GEN-LAST:event_comboTipoContratacionActionPerformed
  
